#include <unistd.h>
#include "libft.h"

void	ft_putnbr(int n)
{
	size_t i;

	if (n < 0)
	{
		if (n == -2147483648)
		{
			write(1, "-2147483648", 11);
			return ;
		}
		i = (n * -1);
		write(1, "-", 1);
	}
	else
		i = n;
	if (i > 9)
	{
		ft_putnbr(i / 10);
		ft_putchar((i % 10) + '0');
	}
	else
		ft_putchar(i + '0');
}
